    private void jBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBorrarActionPerformed

        int fila = jTable1.getSelectedRow();

        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila");
        } else {
            String Nombre = (String) jTable1.getValueAt(fila, 1);

            try {
                
                Connection conn = BaseDatos.conexion();
                PreparedStatement datos = conn.prepareStatement(
                        "select * from Productos where Nombre = '" + Nombre + "'");
                ResultSet exec = datos.executeQuery();
                if (exec.next()) {
                    int codigo = exec.getInt("codigo");
                    String nom = exec.getString("Nombre");
                    double precio = exec.getDouble("Precio");
                    double inventa = exec.getDouble("Inventario");
                    String codPrd = exec.getString("cod_prod");
                    int porcen = exec.getInt("porcentaje");
                     int precioV = exec.getInt("precio_venta");
                    base.borrar(new Producto(codigo, nom, precio, (int) inventa,codPrd,porcen,precioV));

                    rellenar(null);

                    JOptionPane.showMessageDialog(null, "El producto fue borrado");
                }

            } catch (Exception e) {
            }
            
        }


    }//GEN-LAST:event_jBorrarActionPerformed

