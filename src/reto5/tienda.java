    public void rellenar(String nom) {
String consulta;
        try {
            
            Connection conn = BaseDatos.conexion();
            if (nom != null ) {
                consulta = "SELECT cod_prod,Nombre,Precio,Inventario,precio_venta FROM Productos where Nombre like ('"+ nom +"%')";
            }else{
           
                consulta = "SELECT cod_prod,Nombre,Precio,Inventario,precio_venta FROM Productos";
                    }
            Statement result = conn.createStatement();
            ResultSet exec = result.executeQuery(consulta);

            DefaultTableModel modelo = new DefaultTableModel();
            jTable1 = new JTable(modelo);
            jScrollPane1.setViewportView(jTable1);

            modelo.addColumn("codigo producto");
            modelo.addColumn("Nombre");
            modelo.addColumn("Precio comprado");
            modelo.addColumn("Inventario");
            modelo.addColumn("Precio Venta");
            while (exec.next()) {
                Object[] fila = new Object[5];
                for (int i = 0; i < 5; i++) {
                    fila[i] = exec.getObject(i + 1);
                }
                modelo.addRow(fila);
            }
            conn.close();
            

        } catch (SQLException ex) {
          
            JOptionPane.showMessageDialog(null, "Error al llenar la tabla");
        }

    }

